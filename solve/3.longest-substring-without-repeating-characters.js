// @lc code=start
/**
 * @param {string} s
 * @return {number}
 */
var lengthOfLongestSubstring = function(s) {
  let size = s.length
  let dp = new Array(size).fill(-1)
  for (let i = 0; i < size; i++) {
    for (let j = 0; j < i; j++) {
      dp[i] = Math.max(dp[i], dp[j] + 1)
    }
  }
  return dp[size - 1]
};
